export type AdminLocale = "ka" | "en";

export function normalizeAdminLocale(input?: string | string[] | null): AdminLocale {
  const value = Array.isArray(input) ? input[0] : input;
  return value === "en" ? "en" : "ka";
}

export function withAdminLang(path: string, locale: AdminLocale) {
  return `${path}${path.includes("?") ? "&" : "?"}lang=${locale}`;
}

